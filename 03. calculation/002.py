#!
#! Comparison operator.
#!

# Execute with F5.

# A == B
# A != B
# A < B
# A <= B
# A > B
# A >= B

print(5 == 10)

print(5 != 10)

print(5 < 10)

print(5 >= 10)

#!
#! Assignment operator.
#!

a = 1
a = a + 10
print(a)

a = 1
a += 10
print(a)

a = 2
a *= 10
print(a)

a = 10
a /= 3
print(a)

a = 10
a //= 3
print(a)

a = 10
a %= 3
print(a)

#!
#! bool operator.
#!

print(True and False)

print(True or False)

print(not True)

