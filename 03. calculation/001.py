#!
#! Types of arithmetic operations. 
#!

# Execute with F5.

# A + B
# A - B
# A * B
# A ** B
# A / B
# A // B
# A % B

a = 100 + 1
print(a)

a = 200 - 100
print(a)

a = 200 * 100
print(a)

# A raised to the B power.
a = 2 ** 3
print(a)

a = 10 / 3
print(a)

# Decimals are truncated.
a = 10 // 3
print(a)

# remainder.
a = 10 % 3
print(a)
